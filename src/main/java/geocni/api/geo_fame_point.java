package geocni.api;

public class geo_fame_point {
	
	private String fame_user_id;
	
	private int fame_ranking;
	
	private int fame_point_sum;
	
	private int fame_reco_case;
	
	private int fame_shared_case;

	public String getFame_user_id() {
		return fame_user_id;
	}

	public void setFame_user_id(String fame_user_id) {
		this.fame_user_id = fame_user_id;
	}

	public int getFame_ranking() {
		return fame_ranking;
	}

	public void setFame_ranking(int fame_ranking) {
		this.fame_ranking = fame_ranking;
	}

	public int getFame_point_sum() {
		return fame_point_sum;
	}

	public void setFame_point_sum(int fame_point_sum) {
		this.fame_point_sum = fame_point_sum;
	}

	public int getFame_reco_case() {
		return fame_reco_case;
	}

	public void setFame_reco_case(int fame_reco_case) {
		this.fame_reco_case = fame_reco_case;
	}

	public int getFame_shared_case() {
		return fame_shared_case;
	}

	public void setFame_shared_case(int fame_shared_case) {
		this.fame_shared_case = fame_shared_case;
	}

	

}
