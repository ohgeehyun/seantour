package geocni.api;

public class geo_clipboard {
	
    private String clip_id;

    private String clip_title;

    private String clip_page_id;

    private String clip_page_url;

    private String clip_thumb_path;

    private String clip_user_id;

    private String clip_reg_date;

	public String getClip_id() {
		return clip_id;
	}

	public void setClip_id(String clip_id) {
		this.clip_id = clip_id;
	}

	public String getClip_title() {
		return clip_title;
	}

	public void setClip_title(String clip_title) {
		this.clip_title = clip_title;
	}

	public String getClip_page_id() {
		return clip_page_id;
	}

	public void setClip_page_id(String clip_page_id) {
		this.clip_page_id = clip_page_id;
	}

	public String getClip_page_url() {
		return clip_page_url;
	}

	public void setClip_page_url(String clip_page_url) {
		this.clip_page_url = clip_page_url;
	}

	public String getClip_thumb_path() {
		return clip_thumb_path;
	}

	public void setClip_thumb_path(String clip_thumb_path) {
		this.clip_thumb_path = clip_thumb_path;
	}

	public String getClip_user_id() {
		return clip_user_id;
	}

	public void setClip_user_id(String clip_user_id) {
		this.clip_user_id = clip_user_id;
	}

	public String getClip_reg_date() {
		return clip_reg_date;
	}

	public void setClip_reg_date(String clip_reg_date) {
		this.clip_reg_date = clip_reg_date;
	}
}
