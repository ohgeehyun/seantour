package geocni.api;

public class geo_img_files {
	
	private String img_ref_id;
	
	private int img_file_no;
	
	private String img_file_path;
	
	private String img_file_name;
	
	private int img_file_size;
	
	private String img_file_ext;
	
	private String img_created_at;

	public String getImg_ref_id() {
		return img_ref_id;
	}

	public void setImg_ref_id(String img_ref_id) {
		this.img_ref_id = img_ref_id;
	}

	public int getImg_file_no() {
		return img_file_no;
	}

	public void setImg_file_no(int img_file_no) {
		this.img_file_no = img_file_no;
	}

	public String getImg_file_path() {
		return img_file_path;
	}

	public void setImg_file_path(String img_file_path) {
		this.img_file_path = img_file_path;
	}

	public String getImg_file_name() {
		return img_file_name;
	}

	public void setImg_file_name(String img_file_name) {
		this.img_file_name = img_file_name;
	}

	public int getImg_file_size() {
		return img_file_size;
	}

	public void setImg_file_size(int img_file_size) {
		this.img_file_size = img_file_size;
	}

	public String getImg_file_ext() {
		return img_file_ext;
	}

	public void setImg_file_ext(String img_file_ext) {
		this.img_file_ext = img_file_ext;
	}

	public String getImg_created_at() {
		return img_created_at;
	}

	public void setImg_created_at(String img_created_at) {
		this.img_created_at = img_created_at;
	}

}
